    private static boolean editEmployee(java.lang.String arg0, java.lang.String arg1, java.lang.String arg2, java.lang.String arg3) {
        employee_ws.Employee_Service service = new employee_ws.Employee_Service();
        employee_ws.Employee port = service.getEmployeePort();
        return port.editEmployee(arg0, arg1, arg2, arg3);
    }

    private static boolean deleteEmployee(java.lang.String arg0) {
        employee_ws.Employee_Service service = new employee_ws.Employee_Service();
        employee_ws.Employee port = service.getEmployeePort();
        return port.deleteEmployee(arg0);
    }
