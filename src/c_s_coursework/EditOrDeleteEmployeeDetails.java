    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        boolean result = editEmployee(name.getText(), position.getText(), username.getText(), password.getText());
        if (result) {
            JOptionPane.showConfirmDialog(null, "EDITED", "Action Message", JOptionPane.OK_OPTION);
            this.setVisible(false);
            new EmployeeListGUI().setVisible(true);
        } else {
            errorMessage.setText("Sorry Try again");
       }
    }//GEN-LAST:event_editActionPerformed

